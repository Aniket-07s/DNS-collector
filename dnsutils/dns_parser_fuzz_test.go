package dnsutils

import (
	"testing"
)

func FuzzDecodeDNS(f *testing.F) {
	f.Add([]byte{0x00, 0x01, 0x01, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x03, 0x77, 0x77, 0x77, 0x06, 0x67, 0x6f, 0x6f, 0x67, 0x6c, 0x65, 0x03, 0x63, 0x6f, 0x6d, 0x00, 0x00, 0x01, 0x00, 0x01})

	f.Fuzz(func(t *testing.T, data []byte) {
		dm := &DNSMessage{}
		dm.Init()

		// Decode header
		header, err := DecodeDNS(data)
		if err != nil {
			return
		}

		// Decode payload
		dm.DNS.Payload = data
		err = DecodePayload(dm, &header, nil)
		if err != nil {
			return
		}
	})
}
